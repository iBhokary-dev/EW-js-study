/*
 First stuff, let + declaration = the value given to it
*/
let nick = "super man"
let age = 100
let power = "fly-out"
/*
i can change the value given, whenever i want using let
*/
nick = "Batman"
power = "rich"
console.log(
    `Nick: ${nick}
     Age: ${age}
     Poder: ${power}`
)

